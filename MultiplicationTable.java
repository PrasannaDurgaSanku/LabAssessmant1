class MultiplicationTable 
{
    
    
	public static void main(String[] args) 
    
	{
        
		for (int row=1; row<=9; row++)
        
		{
        
			for (int column=1; column<=9; column++)
            
			System.out.println(row*column+ " ");
        
			System.out.println();
        
		}
    
	}
    

}